    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //Metod conecta
        Conexion con = new Conexion();
        Connection conexion = con.conectar();
        
        try{
        
        String query = "INSERT INTO productos(nombre, precio, descripcion) values(?,?,?)";
        PreparedStatement statement = conexion.prepareStatement(query);
        
        //Con un número designamos la posición en la que se colocará el valor de cada casilla correspondiente con ?
        statement.setString(1, textoNombre.getText());
        statement.setInt(2, Integer.parseInt(textoPrecio.getText()));
        statement.setString(3, textoDes.getText());
        
        statement.executeUpdate();
        
        conexion.close();
        
        JOptionPane.showMessageDialog(null, "Producto añadido con éxito");
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al añadir el producto");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        //Creamos un objeto de la clase Conexion
        Conexion con = new Conexion();
        Connection conexion= con.conectar();
        
        int res=0;
        //Variable que recoge el dato del ID insertado para luego borrarlo
        String ident = IDLabel.getText();
        String eliminar =  "DELETE from productos WHERE  id="+ident;
        
        try{
            
           //Llamamos a la función con prepareStatement y recojemos el valor del Upadate para ver si se ha realizado la operación correctamente.
           java.sql.PreparedStatement st = conexion.prepareStatement(eliminar);
            res= st.executeUpdate();
            if (res>0){
                JOptionPane.showMessageDialog(null, "Registro eliminado.", "Eliminado", JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(null, "Registro no encontrado.", "No existe", JOptionPane.INFORMATION_MESSAGE);
            }
            st=null;
            conexion.close();
            
            //Limpiamos las casillas
            IDLabel.setText("");
            textoNombre.setText("");
            textoPrecio.setText("");
            textoDes.setText("");
            
        }
        catch(Exception e){
            System.out.println("Error al eliminar el producto.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

