    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        Conexion con = new Conexion();
        Connection conexion = con.conectar();
        
        try{
            String ref = IDLabel.getText();
            
            //Creamos una función Update  con la misma estructura usada para registros.
            String actualizar = "UPDATE productos set nombre=?,precio=? ,descripcion=? "+" WHERE id="+ref;
            
            java.sql.PreparedStatement st = con.conectar().prepareStatement(actualizar);
            
            
            st.setString(1, textoNombre.getText());
            st.setFloat(2, Float.parseFloat(textoPrecio.getText()));
            st.setString(3, textoDes.getText());
            
            if(st.executeUpdate()>0){
                JOptionPane.showMessageDialog(null, "Registro a"
                        + ""
                        + ""
                        + ""
                        + "ctualizado correctamente");
            }
            else{
                JOptionPane.showMessageDialog(null, "Error al actualizar el registro.");
            }
            
            conexion.close();
            
            IDLabel.setText("");
            textoNombre.setText("");
            textoPrecio.setText("");
            textoDes.setText("");
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al modifficar el registro");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed
    
